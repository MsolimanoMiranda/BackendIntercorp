package com.msolimano.InterCorp.Entity;



public class Kpi {
	
	
	private float promedio;
	private float desviacion_estandar;
	
	
	
	public Kpi() {
		
	}
	
	
	public Kpi(float promedio, float desviacion_estandar) {
		this.promedio = promedio;
		this.desviacion_estandar = desviacion_estandar;
	}
	public float getPromedio() {
		return promedio;
	}
	public void setPromedio(float promedio) {
		this.promedio = promedio;
	}
	public float getDesviacion_estandar() {
		return desviacion_estandar;
	}
	public void setDesviacion_estandar(float desviacion_estandar) {
		this.desviacion_estandar = desviacion_estandar;
	}
	
	
	
	
	
	
	
	
	
	
	

}
